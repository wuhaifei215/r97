<?php
/*-------------------------------------------------
 1️⃣ 先把模块常量和请求参数写好（必须在加载 ThinkPHP 前）
 -------------------------------------------------*/
if (!defined('MODULE_NAME')) {               // 防止 ThinkPHP 再把默认模块改成 Home
    define('MODULE_NAME', 'Pay');            // 强行锁定当前模块为 Pay
}
$_GET['g']     = 'Pay';
$_REQUEST['g'] = 'Pay';

/*-------------------------------------------------
 2️⃣ 定义 Application 目录（指向 /Application/）
 -------------------------------------------------*/
if (!defined('APP_PATH')) {
    // 本文件假设在站点根目录的子目录 /api/
    // dirname(__DIR__) 返回站点根目录（如 /var/www/html）
    define('APP_PATH', dirname(__DIR__) . '/Application/');
}

/*-------------------------------------------------
 3️⃣ 加载 ThinkPHP 正确入口文件
 -------------------------------------------------*/
require dirname(__DIR__) . '/core/ThinkPHP.php';   // 这里写你实际的入口路径

/*-------------------------------------------------
 4️⃣ 调用 Pay 模块下 TreealPay 控制器的 notifyurl 方法
    采用完整路由 R()（一次性实例化 + 执行）
 -------------------------------------------------*/
R('Pay/TreealPay/notifyurl');

/*-------------------------------------------------
 5️⃣ 结束脚本，防止 ThinkPHP 再渲染默认模板
 -------------------------------------------------*/
exit;
