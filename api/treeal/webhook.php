<?php
/* -------------------------------------------------
   1️⃣ 把模块强制设为 Pay（必须在加载 ThinkPHP 前）
   ------------------------------------------------- */
if (!defined('MODULE_NAME')) {
    define('MODULE_NAME', 'Pay');
}
$_GET['g']     = 'Pay';
$_REQUEST['g'] = 'Pay';

/* -------------------------------------------------
   2️⃣ 定义 Application 目录（指向 /Application/）
   ------------------------------------------------- */
if (!defined('APP_PATH')) {
    // 本文件假设在站点根目录的子目录 /api/
    // dirname(__DIR__) 返回站点根目录，例如 /var/www/html
    define('APP_PATH', dirname(__DIR__) . '/Application/');
}

/* -------------------------------------------------
   3️⃣ 加载 ThinkPHP 正确入口
   ------------------------------------------------- */
// 官方默认路径（如果你的框架放在 core/，请改为 '/core/ThinkPHP.php')
require dirname(__DIR__) . '/ThinkPHP/ThinkPHP.php';

/* -------------------------------------------------
   4️⃣ 直接路由调用 Pay 模块下 TreealPay 控制器的 notifyurl
   ------------------------------------------------- */
R('Pay/TreealPay/notifyurl');

/* -------------------------------------------------
   5️⃣ 结束脚本，防止 ThinkPHP 再渲染默认模板
   ------------------------------------------------- */
exit;
